"""## Frequency and Mean for 6 and 12 hours"""

names = ["Lactate", "FiO2", "PaCO2", "pH", "BaseExcess", "Alkalinephos", "AST", "Bilirubin_total",
        "SaO2", "PTT", "Chloride", "Phosphate", "EtCO2"]

def process_features(df, names):
    for name in names:
        df[f'{name}_Freq_6h'] = (
            df.groupby('id')[name]
              .transform(lambda x: x.isna().map({True:0, False:1}).rolling(window=6, min_periods=1).sum())
        )

        df[f'{name}_Freq_12h'] = (
            df.groupby('id')[name]
              .transform(lambda x: x.isna().map({True:0, False:1}).rolling(window=12, min_periods=1).sum())
        )

        filled_values = df.groupby('id')[name].ffill()

        df[f'{name}_Mean_6h'] = (
            filled_values.groupby(df['id'])
            .transform(lambda x: x.rolling(window=6, min_periods=1).mean())
        )

        df[f'{name}_Mean_12h'] = (
        filled_values.groupby(df['id'])
        .transform(lambda x: x.rolling(window=12, min_periods=1).mean())
    )

    return df

